public class valorDaVariaelSOMA {
    
    public static void main(String[]args){

        int indice = 13 , soma = 0, k = 0;

        while (k < indice) {
            k += 1; // incremento 

            soma += k;//incremento

            System.out.println("Resultado da soma "+soma);  // Resposta 91 
        }
    }
}
